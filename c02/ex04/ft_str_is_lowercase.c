int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!(str[i] >= 'a' && str[i] <= 'z')
			&& str[i] != '\0')
		{
			return (0);
		}
		i++;
	}
	return (1);
}
